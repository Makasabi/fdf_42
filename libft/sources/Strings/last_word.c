#include "libft.h"

char	*last_word(char *str, char c)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	while (str[i] != c && i >= 0)
		i--;
	if (i == 0)
		return (str);
	return (str + i + 1);
}
